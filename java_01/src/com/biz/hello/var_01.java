package com.biz.hello;

/*
 * 변수, varriable, value 
 */
public class var_01 {
		
	public static void main(String[] args) {
		/*
		 *  기억장치 메모리 어딘가에
		 *  정수 값을 보관할수있는 곳을 만들고
		 *  거기에 num1이라는 이름표를 붙여라
		 *  찾아갈수 있게  그리고 30을 보관
		 */
		int num1 = 30;
		
		/*
		 * 정수형 변수 num2를 선언하고
		 * 정수40을 할당하라
		 * 
		 * int: 정수형 변수를 선언하는 키워드
		 * num2: 변수형, 기억장소에 붙여진 이름
		 * =: 할당연산자, 오른쪽의 값 왼쪽에 저장
		 */
		int num2 = 40;
		System.out.println(num1 + num2); 
		System.out.println(num2 - num1); 
		System.out.println(num1 * num2);
		System.out.println(num2 / num1); 
		
		
	}
		
 
}
