package com.biz.hello;

public class varboolean_01 {
	
	public static void main(String[] args) {
		System.out.println(3==3); //ture
		System.out.println(3>3);//false
		System.out.println(3<3);//false
		System.out.println(3>3);//false
		System.out.println(3>=3);//ture
		System.out.println(3!=3);//false
		
		/*
		 * boolean 대수
		 * 어떤 값 두개가 가각 true이거나 false
		 * 경우의 값만 가질 수 있는 경우
		 * 이 값을 boolean형 이라고 한다
		 * =========================
		 * A    or    B      c
		 * -------------------------
		 * false false false
		 * false true true
		 * ture false true
		 * ture truee true
		 * =========================
		 * A    and    B      c
		 * -------------------------
		 * false false false
		 * false ture false
		 * ture false false
		 * ture true true
		 * 
		 * 
 		 */
		
		
	
	}

}
